public class Pessoa {
    String nome, endereco;
    int idade;

    public String getNome() {
        return nome;
    }
    public String getEndereco() {
        return endereco;
    }
    public int getIdade() {
        return idade;
    }
    public void setNome(String nome) {
        this.nome = nome;
    }
    public void setEndereco(String endereco) {
        this.endereco = endereco;
    }
    public void setIdade(int idade) {
        this.idade = idade;
    }
}
